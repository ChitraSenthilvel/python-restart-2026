# Topic: Tuples and Sets

# Tuples (immutable)
fruits_tuple = ("apple", "banana", "cherry")
print(fruits_tuple)
print(fruits_tuple[0])

# Sets (unordered, unique)
fruits_set = {"apple", "banana", "cherry", "apple"}
print(fruits_set)  # 'apple' appears only once

# Adding to a set
fruits_set.add("orange")
print(fruits_set)